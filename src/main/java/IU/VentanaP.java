    private void BotonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonModificarActionPerformed
        DefaultTableModel modeloTabla = (DefaultTableModel) tablaCompras.getModel();
        int seleccion = tablaCompras.getSelectedRow();
        int codigo = Integer.parseInt(tablaCompras.getValueAt(seleccion,0).toString());
        if (seleccion<0){

            JOptionPane.showMessageDialog(null,"Debe seleccionar una fila de la tabla");
        }else{
            int confirmar=JOptionPane.showConfirmDialog(null,"¿Estas seguro de Modificar este elemento?");

            if(JOptionPane.OK_OPTION==confirmar){
                modeloTabla.removeRow(seleccion);
                Compras comp1= hashTab.Seleccionar(codigo);

                hashTab.changeNombreHT(comp1,CuadroNombre.getText());
                hashTab.changeFechaHT(comp1,CuadroFecha.getText());
                hashTab.changeValorCuotaHT(comp1,Long.parseLong(CuadroValorCuotas.getText()));
                hashTab.changeNdeCuotasHT(comp1,Integer.parseInt(CuadroCantidadCuotas.getText()));
                Object [] fila = new Object[10];
                fila[0]=comp1.code;
                fila[1]=comp1.nombre;
                fila[2]=comp1.fecha;
                fila[3]=comp1.valorCuota;
                fila[4]=comp1.NdeCuotas;
                fila[5]=comp1.getTotal();
                modeloTabla.addRow(fila);
                tablaCompras.setModel(modeloTabla);
                JOptionPane.showMessageDialog(null,"Modificado correctamente");
                limpiar();


            }
        }
    }//GEN-LAST:event_BotonModificarActionPerformed

