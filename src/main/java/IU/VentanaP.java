    private void BotonAgregarActionPerformed(java.awt.event.ActionEvent evt) {                                             
        // TODO add your handling code here:

        String nombre=CuadroNombre.getText();
        String fecha=CuadroFecha.getText();
        Long ValorDeCuotas=Long.parseLong(CuadroValorCuotas.getText());
        int CantidadDeCuotas =Integer.parseInt(CuadroCantidadCuotas.getText());
        Compras com= new Compras(nombre,fecha,ValorDeCuotas,CantidadDeCuotas,id++);
        hashTab.AgregarHT(com);
        DefaultTableModel modeloTabla = (DefaultTableModel) tablaCompras.getModel();
        Object [] fila = new Object[10];
        fila[0]=com.code;
        fila[1]=com.nombre;
        fila[2]=com.fecha;
        fila[3]=com.valorCuota;
        fila[4]=com.NdeCuotas;
        fila[5]=com.getTotal();
        modeloTabla.addRow(fila);
        tablaCompras.setModel(modeloTabla);
        JOptionPane.showMessageDialog(null,"Agregado correctamente");
        limpiar();
    }

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {
        DefaultTableModel modeloTabla = (DefaultTableModel) tablaCompras.getModel();
        int selec = tablaCompras.getSelectedRow();
        int codigo = Integer.parseInt(tablaCompras.getValueAt(selec,0).toString());
        if (selec < 0){
            JOptionPane.showMessageDialog(null,"Debe seleccionar una fila de la tabla");
        }else{
            int confirmar=JOptionPane.showConfirmDialog(null,"¿Estas seguro de eleminar este elemento?");

            if(JOptionPane.OK_OPTION==confirmar){
            hashTab.EliminarHT(codigo);
            modeloTabla.removeRow(selec);
            JOptionPane.showMessageDialog(null,"Compra eliminada");

        }
        }


    }
