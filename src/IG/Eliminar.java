    private void EliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EliminarMouseClicked
        // TODO add your handling code here:
        Compras aux = compras.find(nombre.getText());
        if(aux==null){
            JOptionPane.showMessageDialog(this, "No existe una compra con este nombre.");
        }else{
            compras.delete(nombre.getText());
        
            Inicio a=new Inicio();
            a.setCompras(compras);
            a.setVisible(rootPaneCheckingEnabled);
        }
        
        compras.delete(nombre.getText());
        
        Inicio a=new Inicio();
        a.setCompras(compras);
        a.setVisible(rootPaneCheckingEnabled);
    }//GEN-LAST:event_EliminarMouseClicked

