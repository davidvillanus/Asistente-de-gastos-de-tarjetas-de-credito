    private void ConsultarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ConsultarMouseClicked
        // TODO add your handling code here:
        // Pasar el nombre a la estructura de datos para obtener sus datos
        Compras aux = compras.find(nombre.getText());
        if(aux==null){
            JOptionPane.showMessageDialog(this, "No existe una compra con este nombre.");
        }else{
            i.setCompras(compras);
            i.setContenido(aux.getNombre(), aux.getFecha(), aux.getValorCuota(), aux.getNdeCuotas());
            i.setVisible(rootPaneCheckingEnabled);
        }
    }//GEN-LAST:event_ConsultarMouseClicked

